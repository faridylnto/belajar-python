# perulangan (loop)

# dengan list
angka2_list = [0,1,2,3,4,5]
for i in angka2_list:
    print(f"nilai i = {i}")
print("akhir dari program 1\n")

# dengan range
angka2_range = range(5)
for i in angka2_range:
    print(f"nilai i = {i}")
print("akhir dari program 2\n")


for i in range(1,10):
    print(f"nilai i = {i}")
print("akhir dari program 3\n")

# menggunakan data string
data_str = "saya farid"
for huruf in data_str:
    print(huruf)
print("akhir dari program 4\n")